import numpy as np 
#  Sort the arrays numbers
Array_list  =np.array([8,3,4,5,1,7,9,2,6])
sorting =np.sort(Array_list)
print ("Sort the array : ",sorting)

#  Sort the arrays String
Array_String  =np.array(['kaviya','manisha','abi','bavi'])
sorting =np.sort(Array_String)
print ("Sort the array of string  : ",sorting)

#  Sorting a 2-D Array
# Both array will be sorted .
array2D = np.array([[8,9,7], [3,2,1]])
print ("Sorting the 2-D arrys : ")
print (np.sort(array2D))

